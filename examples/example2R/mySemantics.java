
// Example 2R

class mySemantics extends mouse.runtime.SemanticsBase
{
  //-------------------------------------------------------------------
  //  Compute = Sum !_
  //           0
  //-------------------------------------------------------------------
  void print()
    { System.out.println((Integer)rhs(0).get()); }

  //-------------------------------------------------------------------
  //  Sum = Sum "+" Number
  //         0   1    2
  //-------------------------------------------------------------------
  void sum()
    { lhs().put((Integer)rhs(0).get() + (Integer)rhs(2).get()); }

  //-------------------------------------------------------------------
  //  Sum = Number
  //          0
  //-------------------------------------------------------------------
  void pass()
    { lhs().put(rhs(0).get()); }

  //-------------------------------------------------------------------
  //  Number = [0-9] ... [0-9]
  //             0        n-1
  //-------------------------------------------------------------------
  void number()
    { lhs().put(Integer.valueOf(lhs().text())); }
}

